let rev = {};
Object.keys(map).forEach((txt) => {
  if (rev[map[txt]]) {
    throw new Error('Double mapped symbol ' + txt);
  }
  rev[map[txt]] = txt;
});

function text2sym(txt) {
  return map[txt] || txt;
}

function sym2text(sym) {
  if (rev[sym]) {
    return rev[sym];
  }
  throw new Error('Invalid currency symbol ' + sym);
}

module.exports = {
  txt2sym: text2sym,
  sym2text: sym2text
};
